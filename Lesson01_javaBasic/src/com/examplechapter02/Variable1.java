package com.examplechapter02;

public class Variable1 {

	public static void main(String[] args) {
		//여기부터 코딩시작
		//변수란?데이터를 보관하는 기억장소의 이름
		//변수 선언 구문
		//타입 변수명
		int level; //int : 정수형 level:변수명
	
		level = 10;
		// = 대입연산자 assignment operator
		//변수 level에 정수형 숫자 값 10을 대합하여라(보관하여라)
		System.out.println(level);
//System클래스의 out의 println함수를 이용하여 level을 출력하여라
	}// end of main()

}// end of class Variabale1
